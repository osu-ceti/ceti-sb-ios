import UIKit
import ObjectMapper

/**
 * ErrorBean Class to store ErrorBean details
 * returned from REST API JSON
 */
class ClaimEventBean: Mappable {
    
    var message: String?
    
    var state: Int?
    
    var status: Int?
    
    required init()
    {
        //Do not remove required for Initialization
    }
    
    required init?(_ map: Map) {
        mapping(map)
    }
    
    // Mappable
    func mapping(map: Map) {
        
        message    <- map["message"]
        
        state    <- map["state"]
        
        status    <- map["status"]
        
    }
}
